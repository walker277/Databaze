CREATE VIEW cizinci_v_zapasech AS 
SELECT k.nazev, h.jmeno, h.prijmeni, h.narodnost, s.prvni_minuta_sestavy_v_zapase, s.posledni_minuta_sestavy_v_zapase, z.datum
	FROM HRACI h, SESTAVA_V_ZAPASE s, ZAPAS z, HRAC_PUSOBICI_V_KLUBU hp, KLUBY k
WHERE hp.id_hrace_pusobiciho_v_klubu = s.HRAC_PUSOBICI_V_KLUBU_id_hrace_pusobiciho_v_klubu AND s.ZAPAS_id_zapasu = z.id_zapasu AND hp.HRACI_id_hrace = h.id_hrace AND h.narodnost != 'Česká republika' AND hp.KLUBY_id_klubu = k.id_klubu;


CREATE VIEW tabulka_strelcu AS 
SELECT k.nazev, hr.jmeno, hr.prijmeni, COUNT(u.typ) AS pocet_golu
	FROM KLUBY k, HRAC_PUSOBICI_V_KLUBU h, UDALOST u, SESTAVA_V_ZAPASE s, HRACI hr 
WHERE u.SESTAVA_V_ZAPASE_id_sestavy = s.id_sestavy AND h.id_hrace_pusobiciho_v_klubu = s.HRAC_PUSOBICI_V_KLUBU_id_hrace_pusobiciho_v_klubu AND  hr.id_hrace = h.id_hrace_pusobiciho_v_klubu AND k.id_klubu = h.KLUBY_id_klubu AND u.typ LIKE 'Gól%'
GROUP BY hr.id_hrace, k.nazev, hr.jmeno, hr.prijmeni
ORDER BY pocet_golu DESC;
